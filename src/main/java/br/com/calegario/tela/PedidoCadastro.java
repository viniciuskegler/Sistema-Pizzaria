    private void btPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btPesquisarActionPerformed
        String telefone = varTelefone.getText().trim();
        if (telefone.length() != 14) {
            JOptionPane.showMessageDialog(null, "Digite um telefone vÃ¡lido.");
        } else {
            try {
                session = HibernateUtil.abrirConexao();
                cliente = clienteDao.pesquisarPorTelefone(telefone, session);
                pnPedido.setVisible(true);
                lb_mostrarNomeCliente.setText(cliente.getNome());
                lb_mostrarNumPedidos.setText(String.valueOf(cliente.getPedidos().size()));
                lb_mostrarValorTotal.setText(String.valueOf(calcularVlTotal(cliente.getPedidos())));
            } catch (Exception e) {
                System.out.println("Erro ao pesquisar cliente.");
            } finally {
                session.close();
            }
        }
    }//GEN-LAST:event_btPesquisarActionPerformed

    private Double calcularVlTotal(List<Pedido> pedidos) {
        Double vlTotal = 0.0;
        for (Pedido pedido : pedidos) {
            vlTotal += pedido.getValorTotal().doubleValue();
        }
        return vlTotal;
    }

    private void atualizarInfos() {
        try {
            session = HibernateUtil.abrirConexao();
            cliente = clienteDao.pesquisarPorTelefone(varTelefone.getText(), session);
            lb_mostrarNomeCliente.setText(cliente.getNome());
            lb_mostrarNumPedidos.setText(String.valueOf(cliente.getPedidos().size()));
            lb_mostrarValorTotal.setText(String.valueOf(calcularVlTotal(cliente.getPedidos())));
        } catch (Exception e) {
            System.out.println("Erro ao atualizar informaÃ§Ãµes.");
        } finally {
            session.close();
        }
    }

